#Implementation of Operators using Python 
a = int(input("Enter 1st No: ")) 
b = int(input("Enter 2nd No: ")) 
c = a+b 
d = a-b 
e = a*b 
f = a/b 
g = a%b 
h = a**b 
i = a//b 
j = a&b 
k = a|b 
l = a^b 
print("Answers of Arithmetic  Operations:\nSum={}\nDiff={}\nMul={}\nDiv={}\nMod={}\nExpo={}\nFloat  Div={}\n".format(c,d,e,f,g,h,i)) 
print("Answers of Bitwise Operations:\nBitwise AND={}\nBitwise OR={}\nBitwise  NOR={}".format(j,k,l)) 
